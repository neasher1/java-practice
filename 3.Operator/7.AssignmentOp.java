
package javaapplication1;

public class AssignOp3 {
    public static void main(String[] args) {
        
        int x = 3, y =6;
        
        x+=y; //x=9
        System.out.println("X = "+x);
        
        x-=y; //x=3
        System.out.println("X = "+x);
        
        x*=y; //x=18
        System.out.println("X = "+x);
        
        x/=y; //x=3
        System.out.println("X = "+x);
        
        x%=y; //x=3
        System.out.println("X = "+x);
        
        
    }
}


