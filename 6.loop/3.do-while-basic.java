
package javaapplication1;

public class DoWhile19 {
    public static void main(String[] args) {
       
        int i=1;
        do{
            
            System.out.println(i+".Hello,WOrld!");
            i++;
            
        }while(i<=10);
        System.out.println("");
       
        int j=10;
        do{
            
            System.out.println(j+".Hello,WOrld!");
            j--;
            
        }while(j>=1);
        System.out.println("");
       
        int k=1;
        do{
            
            System.out.println(k);
            k=k+2;
            
        }while(k<=10);
        System.out.println("");
       
        int l=2;
        do{
            
            System.out.println(l);
            l=l+2;
            
        }while(l<=10);
       
    }
}
