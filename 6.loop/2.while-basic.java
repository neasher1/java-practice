
package javaapplication1;

public class WhileLoop18 {
    public static void main(String[] args) {
       
        int i=1;
        while(i<=10){
        
            System.out.println(i+".Hello, World!");
            i++;
        
        }System.out.println("");
        
        int j=10;
        while(j>=1){
        
            System.out.println(j+".Hello, World!");
            j--;
        
        }System.out.println("");
        
        int k=1;
        while(k<=10){
        
            System.out.println(k);
            k=k+2;
        
        }
        System.out.println("");
        
        int l=2;
        while(l<=10){
        
            System.out.println(l);
            l=l+2;
        
        }
        
    }
}
