package javaapplication1;

public class ForLoop18 {
    public static void main(String[] args) {
       
        for(int i=1; i<=10; i++){
        
            System.out.println(i+".Hello World!");
        
        }
        System.out.println("");
        for(int i=10; i>=1; i--){
        
            System.out.println(i+".Hello World!");
        
        }
        System.out.println("");
        
        for(int i=1; i<=10; i++){
        
            System.out.println(i);
        
        }
        System.out.println("");
        
        for(int i=1; i<=10; i=i+2){
        
            System.out.println(i);
        
        }
        System.out.println("");
        
        for(int i=2; i<=10; i=i+2){
        
            System.out.println(i);
        
        }
        
    }
}
